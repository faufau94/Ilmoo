const categoryCache = new Map<string, string>();

async function getCategoryId(slug: string): Promise<string | null> {
  if (categoryCache.has(slug)) return categoryCache.get(slug)!;
  const result = await pool.query('SELECT id FROM categories WHERE slug = $1', [slug]);
  const id = result.rows[0]?.id ?? null;
  if (id) categoryCache.set(slug, id);
  return id;
}

async function seed() {
  console.log('Seeding questions (batch 2: Cinema, Series TV, Musique, Art & Litterature)...\n');

  let inserted = 0;
  let skipped = 0;

  for (const q of questions) {
    const categoryId = await getCategoryId(q.subcategory_slug);
    if (!categoryId) {
      console.warn(`  Category not found: ${q.subcategory_slug}, skipping`);
      skipped++;
      continue;
    }

    await pool.query(
      `INSERT INTO questions (category_id, question_text, answers, correct_index, difficulty, explanation, is_active, is_verified)
       VALUES ($1, $2, $3, $4, $5, $6, true, true)
       ON CONFLICT DO NOTHING`,
      [categoryId, q.question_text, JSON.stringify(q.answers), q.correct_index, q.difficulty, q.explanation ?? null],
    );
    inserted++;
  }

  console.log(`Done: ${inserted} questions inserted, ${skipped} skipped.`);
  await pool.end();
}

seed().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
